#operator

a = 3
b = 4
# Arithimetics operator

print(".........Arithimetics operator........")

print("+ of ",a, " , ", b,  " = ",a+b)
print("- of ",a, " , ", b,  " = ",a-b)
print("* of ",a, " , ", b,  " = ",a*b)
print("/ of ",a, " , ", b,  " = ",a/b)

# Assignment operator
print(".........Assignment operator........")

a = 34
print("Original value of a = ",a)
a += 2 
print("+= value of a = ",a)
a -= 2 
print("-= value of a = ",a)
a *= 2 
print("*= value of a = ",a)
a /= 2 
print("/= value of a = ",a)

# Comparison operator
print(".........Comparison operator........")

print("> of ",a, " , ", b,  " = ",a>b)
print("< of ",a, " , ", b,  " = ",a<b)
print("== of ",a, " , ", b,  " = ",a==b)
print(">= of ",a, " , ", b,  " = ",a>=b)
print("<= of ",a, " , ", b,  " = ",a<=b)
print("!= of ",a, " , ", b,  " = ",a!=b)

# Logical operator
print(".........Logical operator........")

bool1 = True
bool2 = False

print("and  of ",bool1, " , ", bool2,  " = ",bool1 and bool2)
print("or  of ",bool1, " , ", bool2,  " = ",bool1 or bool2)
print("not  of ",bool1,  " = ",not bool1)
